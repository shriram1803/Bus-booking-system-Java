        int temp = Integer.valueOf(get_seater.getText());
        int temp1 = Integer.valueOf(get_sleeper.getText());
        if(seatercnt < temp || sleepercnt < temp1){
            JOptionPane.showMessageDialog(this, "Seat count exceeded!!");
            BookTicket2 obj = new BookTicket2();
            obj.getTicketDetails(getcid,getbid);
            obj.setVisible(true);
            dispose();
        }else{
        int getseat = temp;
        int getsleep = temp1;
        int res = getsleep*sleeperamt + getseat*seateramt;
        BookTicket3 obj2 = new BookTicket3();
obj2.setValues(res, getcid, getbid, getseat, getsleep);
obj2.setVisible(true); // Open the Second.java window
	dispose(); }
        
