    public void getTicketDetails(int cid, int bid){
        getcid = cid;
        getbid = bid;
        ResultSet bs = null;
        try{  
Class.forName("com.mysql.cj.jdbc.Driver");  
//here sonoo is database name, root is username and password
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mydb","root","password");
                //here sonoo is database name, root is username and password
                Statement stmt=con.createStatement();
                bs=stmt.executeQuery("select * from busdetails where bid=" +bid);
                if(bs.next()){
                    
                        seatercnt = bs.getInt(2);
                        sleepercnt = bs.getInt(3);
                        seateramt = bs.getInt(4);
                        sleeperamt = bs.getInt(5);   
        seater_nos.setText(String.valueOf(seatercnt));
        sleeper_nos.setText(String.valueOf(sleepercnt));                                        
                }   else{
                    JOptionPane.showMessageDialog(this, "Invalid Busid!!");
                }        
}catch(Exception e){ System.out.println(e);} 

        
        

    }

